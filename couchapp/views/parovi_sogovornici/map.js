function(doc) {
    var прв, втор;
    if (doc.соговорници[0].иницијали < doc.соговорници[1].иницијали) {
        прв = doc.соговорници[0];
        втор = doc.соговорници[1];
    }
    else {
        прв = doc.соговорници[1];
        втор = doc.соговорници[0];
    }
    emit(прв.назив.join(" ") + " и " + втор.назив.join(" "), null);
}
